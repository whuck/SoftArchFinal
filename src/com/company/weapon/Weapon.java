package com.company.weapon;
/**
 * Weapon Parent abstract class
 *  @author Will Huck
 */
public abstract class Weapon {
    /**
     * gets Weapons damage
     * @return int representing the Weapons Damage
     */
    public abstract int getDamage();
    /**
     * gets Weapons Name
     * @return string representing Weapons name
     */
    public abstract String getName();
}
